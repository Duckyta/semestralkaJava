package abstr.zasobnik;

/**
 *
 * @author DuckMcQuack
 * @param <E>
 */
public interface IZasobnik<E> {

    /**
     * @return
     */
    boolean jePrazdny();

    /**
     * Metoda oodebere z kolekce data
     *
     * @return vraci referenci na data odebiraneho prvku
     */
    E odeber();

    /**
     * Metoda vklada data
     *
     * @param element
     */
    void vloz(E element);

    /**
     * Zrusi frontu
     */
    void zrus();

}
