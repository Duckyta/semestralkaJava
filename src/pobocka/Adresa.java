package pobocka;

import java.io.Serializable;

/**
 *
 * @author Karel
 */
public class Adresa implements Serializable{
   private final String ulice;
   private final int cislo;
   private final String obec;

   public Adresa(String ulice, int cislo, String obec) {
      this.ulice = ulice;
      this.cislo = cislo;
      this.obec = obec;
   }

   @Override
   public String toString() {
      return  " ulice=" + ulice + ", cislo=" + cislo + ", obec=" + obec ;
   }
   
   
}
